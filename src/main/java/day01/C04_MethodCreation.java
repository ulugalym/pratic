package day01;

public class C04_MethodCreation {
    public static void main(String[] args) {
        //konsola;
       /*
        hello world
        Hello World
        HEllo WOrld
        HELlo WORld
        HELLo WORLd
        HELLO WORLD
        yazdırın
       */
      yazdir();
      yazdir();
      yazdir();
    }
    //method olusturulurkan syntax
    //access modifier + static +return type +method ismi (){}
    public static void yazdir (){
        System.out.println("hello world");
        System.out.println("Hello World");
        System.out.println("HEllo WOrld");
        System.out.println("HELlo WORld");
        System.out.println("HELLo WORLd");
        System.out.println("HELlO WORlD");
    }


}
